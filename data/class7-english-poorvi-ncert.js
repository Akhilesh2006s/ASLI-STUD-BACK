function norm(s) {
  return String(s || '')
    .trim()
    .toLowerCase()
    .replace(/\s+/g, ' ');
}

export function isClass7EnglishSubject(subjectId) {
  const s = norm(subjectId);
  return s.includes('english');
}

export function matchClass7EnglishChapter(topicId) {
  return matchNcertChapter(topicId, CLASS_7_ENGLISH_POORVI_CHAPTERS);
}
